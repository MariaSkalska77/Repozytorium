public class Car {


        private String genre;
        private String brand;


    @Override
    public String toString() {
        return "Car{" +
                "genre='" + genre + '\'' +
                ", brand='" + brand + '\'' +
                '}';

    }

    public Car(String genre, String brand){
            this.genre = genre;
            this.brand = brand;








        }






}
